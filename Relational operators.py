print("Relational Operator")

a = 96
b = 66

# Equal to (==)
if (a == b):
    print("You are not correct")
else:
    print("You are correct")

# Not equal to (!=)
if (a != b):
    print("You are correct")
else:
    print("You are wrong")

# Greater than (>)
if (a > b):
    print("You are correct")
else:
    print("You are wrong")

# Less than (<)
if (a < b):
    print("You are correct")
else:
    print("You are wrong")

# Less than or equal to (<=)
if (a <= b):
    print("You are correct")
else:
    print("You are wrong")

# Greater than or equal to (>=)
if (a >= b):
    print("You are correct")
else:
    print("You are wrong")